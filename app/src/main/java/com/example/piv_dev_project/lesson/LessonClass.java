package com.example.piv_dev_project.lesson;

public class LessonClass {
    String lesson;
    String title;
    String room;
    String date;
    String time;
    String mapsLink;
    public LessonClass(String lesson, String title, String room, String date, String time, String mapsLink) {
        this.lesson = lesson;
        this.title = title;
        this.room = room;
        this.date = date;
        this.time = time;
        this.mapsLink = mapsLink;
    }
}
